#!/usr/bin/env node

const fs = require('node:fs');
const path = require('node:path');
const { execFileSync } = require('node:child_process');
const ts = require('typescript');

const LOADABLES_DIR = path.resolve(__dirname, '../src/perfs/loadables');
const GENERATED_ALIAS_FILE = path.resolve(
  __dirname,
  './loadables-aliases.generated.cjs',
);
const DEV_SUFFIX = '.dev.ts';
const PROD_SUFFIX = '.prod.ts';
const DEFAULT_SUFFIX = '.ts';

function main() {
  const argv = new Set(process.argv.slice(2));
  const shouldStage = argv.has('--stage');
  const shouldCheck = argv.has('--check');

  const loadableBaseNames = getLoadableBaseNames();
  const prodFiles = loadableBaseNames.map(
    baseName => `${baseName}${PROD_SUFFIX}`,
  );

  let didChangeAny = false;

  for (const prodFilename of prodFiles) {
    const prodFilePath = path.join(LOADABLES_DIR, prodFilename);
    const baseName = prodFilename.slice(0, -PROD_SUFFIX.length);
    const devFilename = prodFilename.replace(PROD_SUFFIX, DEV_SUFFIX);
    const devFilePath = path.join(LOADABLES_DIR, devFilename);
    const defaultFilename = `${baseName}${DEFAULT_SUFFIX}`;
    const defaultFilePath = path.join(LOADABLES_DIR, defaultFilename);
    const prodSource = fs.readFileSync(prodFilePath, 'utf8');
    const nextDevSource = renderDevSource({
      prodFilename,
      prodSource,
    });
    const nextDefaultSource = renderDefaultSource({ prodFilename });
    const prevDevSource = fs.existsSync(devFilePath)
      ? fs.readFileSync(devFilePath, 'utf8')
      : null;
    const prevDefaultSource = fs.existsSync(defaultFilePath)
      ? fs.readFileSync(defaultFilePath, 'utf8')
      : null;

    if (prevDevSource !== nextDevSource) {
      fs.writeFileSync(devFilePath, nextDevSource);
      didChangeAny = true;
      console.log(
        `[loadables] updated ${path.relative(process.cwd(), devFilePath)}`,
      );
    }

    if (prevDefaultSource !== nextDefaultSource) {
      fs.writeFileSync(defaultFilePath, nextDefaultSource);
      didChangeAny = true;
      console.log(
        `[loadables] updated ${path.relative(process.cwd(), defaultFilePath)}`,
      );
    }
  }

  const nextAliasSource = renderGeneratedAliasSource({
    loadableBaseNames,
  });
  const prevAliasSource = fs.existsSync(GENERATED_ALIAS_FILE)
    ? fs.readFileSync(GENERATED_ALIAS_FILE, 'utf8')
    : null;

  if (prevAliasSource !== nextAliasSource) {
    fs.writeFileSync(GENERATED_ALIAS_FILE, nextAliasSource);
    didChangeAny = true;
    console.log(
      `[loadables] updated ${path.relative(
        process.cwd(),
        GENERATED_ALIAS_FILE,
      )}`,
    );
  }

  if (shouldStage) {
    execFileSync(
      'git',
      [
        'add',
        '-A',
        'src/perfs/loadables',
        'scripts/loadables-aliases.generated.cjs',
      ],
      {
        cwd: path.resolve(__dirname, '..'),
        stdio: 'inherit',
      },
    );
  }

  if (shouldCheck && didChangeAny) {
    process.exitCode = 1;
  }
}

function renderDevSource({ prodFilename, prodSource }) {
  const sourceFile = ts.createSourceFile(
    prodFilename,
    prodSource,
    ts.ScriptTarget.Latest,
    true,
    ts.ScriptKind.TS,
  );

  const renderedStatements = [];

  for (const statement of sourceFile.statements) {
    if (ts.isImportDeclaration(statement)) {
      continue;
    }

    if (ts.isExportDeclaration(statement)) {
      renderedStatements.push(statement.getText(sourceFile).trim());
      continue;
    }

    if (ts.isVariableStatement(statement) && hasExportModifier(statement)) {
      for (const declaration of statement.declarationList.declarations) {
        renderedStatements.push(
          renderRegisterAppScreenDeclaration({
            declaration,
            sourceFile,
            prodFilename,
          }),
        );
      }
      continue;
    }

    throw new Error(
      `[loadables] Unsupported statement in ${prodFilename}: ${
        ts.SyntaxKind[statement.kind]
      }`,
    );
  }

  const header = [
    '/* eslint-disable */',
    `// AUTO-GENERATED FROM ./${prodFilename} BY \`yarn workspace rabby-mobile loadables:generate\`.`,
    '// DO NOT EDIT THIS FILE DIRECTLY.',
  ].join('\n');

  return `${header}\n\n${renderedStatements.join('\n')}\n`;
}

function renderDefaultSource({ prodFilename }) {
  const baseName = prodFilename.slice(0, -PROD_SUFFIX.length);
  const header = [
    '/* eslint-disable */',
    `// AUTO-GENERATED FROM ./${prodFilename} BY \`yarn workspace rabby-mobile loadables:generate\`.`,
    '// DO NOT EDIT THIS FILE DIRECTLY.',
  ].join('\n');

  return `${header}\n\nexport * from './${baseName}.prod';\n`;
}

function renderGeneratedAliasSource({ loadableBaseNames }) {
  const prodEntries = loadableBaseNames.map(baseName => [
    `@/perfs/loadables/${baseName}`,
    `./src/perfs/loadables/${baseName}.prod`,
  ]);
  const devEntries = loadableBaseNames.map(baseName => [
    `@/perfs/loadables/${baseName}`,
    `./src/perfs/loadables/${baseName}.dev`,
  ]);

  const renderEntries = entries =>
    entries.map(([key, value]) => `    '${key}': '${value}',`).join('\n');

  return `/* eslint-disable */
// AUTO-GENERATED BY \`yarn workspace rabby-mobile loadables:generate\`.
// DO NOT EDIT THIS FILE DIRECTLY.

module.exports = {
  dev: {
${renderEntries(devEntries)}
  },
  prod: {
${renderEntries(prodEntries)}
  },
};
`;
}

function getLoadableBaseNames() {
  return fs
    .readdirSync(LOADABLES_DIR)
    .filter(name => name.endsWith(PROD_SUFFIX))
    .map(name => name.slice(0, -PROD_SUFFIX.length))
    .sort();
}

function hasExportModifier(node) {
  return !!node.modifiers?.some(
    modifier => modifier.kind === ts.SyntaxKind.ExportKeyword,
  );
}

function renderRegisterAppScreenDeclaration({
  declaration,
  sourceFile,
  prodFilename,
}) {
  if (!ts.isIdentifier(declaration.name)) {
    throw new Error(
      `[loadables] Unsupported exported declaration name in ${prodFilename}: ${declaration.name.getText(
        sourceFile,
      )}`,
    );
  }

  const exportName = declaration.name.text;
  const initializer = declaration.initializer;

  if (!initializer || !ts.isCallExpression(initializer)) {
    throw new Error(
      `[loadables] ${prodFilename}#${exportName} must be initialized by registerAppScreen(...)`,
    );
  }

  if (!ts.isIdentifier(initializer.expression)) {
    throw new Error(
      `[loadables] ${prodFilename}#${exportName} has unsupported initializer expression`,
    );
  }

  if (initializer.expression.text !== 'registerAppScreen') {
    throw new Error(
      `[loadables] ${prodFilename}#${exportName} must call registerAppScreen(...)`,
    );
  }

  const [configArg] = initializer.arguments;
  if (!configArg || !ts.isObjectLiteralExpression(configArg)) {
    throw new Error(
      `[loadables] ${prodFilename}#${exportName} must pass an object config to registerAppScreen(...)`,
    );
  }

  const loaderProp = configArg.properties.find(
    property =>
      ts.isPropertyAssignment(property) &&
      getPropertyNameText(property.name, sourceFile) === 'loader',
  );

  if (!loaderProp || !ts.isPropertyAssignment(loaderProp)) {
    throw new Error(
      `[loadables] ${prodFilename}#${exportName} is missing a loader property`,
    );
  }

  const loaderFunction = unwrapExpression(loaderProp.initializer);

  if (
    !ts.isArrowFunction(loaderFunction) &&
    !ts.isFunctionExpression(loaderFunction)
  ) {
    throw new Error(
      `[loadables] ${prodFilename}#${exportName} loader must be a function expression`,
    );
  }

  const loadType = getJSDocTagValue(loaderFunction, 'loadtype');
  const loadTarget = analyzeLoaderFunction({
    loaderFunction,
    sourceFile,
    prodFilename,
    exportName,
    forceDefaultExport: loadType === 'iife',
  });

  if (loadTarget.kind === 'default') {
    return `export { default as ${exportName} } from '${loadTarget.modulePath}';`;
  }

  if (loadTarget.exportName === exportName) {
    return `export { ${loadTarget.exportName} } from '${loadTarget.modulePath}';`;
  }

  return `export { ${loadTarget.exportName} as ${exportName} } from '${loadTarget.modulePath}';`;
}

function getPropertyNameText(nameNode, sourceFile) {
  if (ts.isIdentifier(nameNode) || ts.isStringLiteral(nameNode)) {
    return nameNode.text;
  }

  return nameNode.getText(sourceFile);
}

function getJSDocTagValue(node, tagName) {
  const tag = ts
    .getJSDocTags(node)
    .find(item => item.tagName.text.toLowerCase() === tagName.toLowerCase());

  if (!tag) {
    return '';
  }

  if (typeof tag.comment === 'string') {
    return tag.comment.trim();
  }

  if (Array.isArray(tag.comment)) {
    return tag.comment
      .map(item => item.text)
      .join('')
      .trim();
  }

  return '';
}

function analyzeLoaderFunction({
  loaderFunction,
  sourceFile,
  prodFilename,
  exportName,
  forceDefaultExport,
}) {
  const body = unwrapExpression(loaderFunction.body);

  if (!ts.isCallExpression(body)) {
    throw new Error(
      `[loadables] ${prodFilename}#${exportName} loader body must return import(...) or import(...).then(...)`,
    );
  }

  if (isDynamicImportCall(body)) {
    return {
      kind: 'default',
      modulePath: getImportPath(body),
    };
  }

  if (
    ts.isPropertyAccessExpression(body.expression) &&
    body.expression.name.text === 'then'
  ) {
    const importCall = unwrapExpression(body.expression.expression);

    if (!ts.isCallExpression(importCall) || !isDynamicImportCall(importCall)) {
      throw new Error(
        `[loadables] ${prodFilename}#${exportName} loader has unsupported .then(...) target`,
      );
    }

    const modulePath = getImportPath(importCall);

    if (forceDefaultExport) {
      return {
        kind: 'default',
        modulePath,
      };
    }

    const [mapperFn] = body.arguments;

    if (
      !mapperFn ||
      (!ts.isArrowFunction(mapperFn) && !ts.isFunctionExpression(mapperFn))
    ) {
      throw new Error(
        `[loadables] ${prodFilename}#${exportName} loader .then(...) must receive a mapper function`,
      );
    }

    return {
      kind: 'named',
      modulePath,
      exportName: extractMappedExportName({
        mapperFn,
        sourceFile,
        prodFilename,
        exportName,
      }),
    };
  }

  throw new Error(
    `[loadables] ${prodFilename}#${exportName} loader must return import(...) or import(...).then(...)`,
  );
}

function unwrapExpression(expression) {
  let current = expression;

  while (
    ts.isParenthesizedExpression(current) ||
    ts.isAsExpression(current) ||
    ts.isTypeAssertionExpression(current) ||
    ts.isSatisfiesExpression?.(current)
  ) {
    current = current.expression;
  }

  return current;
}

function isDynamicImportCall(expression) {
  return (
    ts.isCallExpression(expression) &&
    expression.expression.kind === ts.SyntaxKind.ImportKeyword &&
    expression.arguments.length === 1 &&
    ts.isStringLiteral(expression.arguments[0])
  );
}

function getImportPath(importCall) {
  return importCall.arguments[0].text;
}

function extractMappedExportName({
  mapperFn,
  sourceFile,
  prodFilename,
  exportName,
}) {
  const mapperBody = unwrapExpression(mapperFn.body);

  if (!ts.isObjectLiteralExpression(mapperBody)) {
    throw new Error(
      `[loadables] ${prodFilename}#${exportName} .then(...) mapper must return an object literal`,
    );
  }

  const defaultProp = mapperBody.properties.find(
    property =>
      ts.isPropertyAssignment(property) &&
      getPropertyNameText(property.name, sourceFile) === 'default',
  );

  if (!defaultProp || !ts.isPropertyAssignment(defaultProp)) {
    throw new Error(
      `[loadables] ${prodFilename}#${exportName} .then(...) mapper must return { default: ... }`,
    );
  }

  const defaultExpr = unwrapExpression(defaultProp.initializer);

  if (ts.isPropertyAccessExpression(defaultExpr)) {
    return defaultExpr.name.text;
  }

  if (ts.isIdentifier(defaultExpr)) {
    return defaultExpr.text;
  }

  throw new Error(
    `[loadables] ${prodFilename}#${exportName} .then(...) mapper default target is unsupported`,
  );
}

main();
